--create a new database---
CREATE DATABASE Health_and_Fitness;

--Create users table
CREATE TABLE Users (
		user_id int PRIMARY KEY IDENTITY(1,1),
		usernmae varchar(50) NOT NULL,
		email varchar(100) UNIQUE NOT NULL,
		date_of_birth DATE,
		gender varchar(10),
		signup_date DATETIME DEFAULT GETDATE()
);

--Create workouts table
CREATE TABLE Workouts  (
		workout_id int PRIMARY KEY IDENTITY(1,1),
		user_id int,
		workout_date DATE,
		duration_minutes int,
		calories_burned int,
		FOREIGN KEY (user_id) REFERENCES Users(user_id)
);

--Create Exercises table
CREATE TABLE Exercises (
		exercise_id int PRIMARY KEY IDENTITY(1,1),
		workout_id int,
		exercise_name varchar(100),
		sets int,
		reps int,
		weight_kg DECIMAL(5, 2),
		FOREIGN KEY (workout_id) REFERENCES Workouts(workout_id)
);

--Create Nutrition table
CREATE TABLE Nutrition (
		nutrition_id int PRIMARY KEY IDENTITY(1,1),
		user_id int,
		log_date DATE,
		calories_consumed int,
		protein_g DECIMAL(5,2),
		carbs_g DECIMAL(5,2),
		fats_g DECIMAL(5, 2),
		FOREIGN KEY (user_id) REFERENCES Users(user_id)
);




